class gameGUI(gym.Env):
    def __init__(self, totalSize, cellSize, agentSize, title=''):
        self.totalSize = totalSize
        self.cellSize = cellSize
        self.agentSize = agentSize
        self.is_single=self.agentSize==1
        self.viewer = None
        self.obstacles = numpy.zeros([totalSize, totalSize]).astype(numpy.int32)
        self.agents = numpy.zeros([agentSize, 2]).astype(numpy.int32)
        self.goals = numpy.zeros([agentSize, 2]).astype(numpy.int32)
        self.goal_places = numpy.zeros([totalSize, totalSize]).astype(numpy.int32)
        self.agent_places = numpy.zeros([totalSize, totalSize]).astype(numpy.int32)

        if(self.is_single):
            self.action_space=spaces.Discrete(4)
            self.observation_space=Box(low=0, high=1, shape=(64, 64, 3), dtype=numpy.int32)
        else:
            for i in range(len(self.agents)):
                self.action_space.append(spaces.Discrete(4))
                self.observation_space.append(Box(low=0, high=1, shape=(64, 64, 3), dtype=numpy.int32))

        self.seed()

    def seed(self, seed=None):
        self.np_random, seed = seeding.np_random(seed)
        return [seed]

    def _fillSquare(self,left, top, color):
        length=self.cellSize
        left*=self.cellSize
        top*=self.cellSize
        x = rendering.FilledPolygon([(left,top),(left+length,top),(left+length,top+length),(left,top+length)])
        x.set_color(color[0]/255.0,color[1]/255.0,color[2]/255.0)
        self.viewer.add_geom(x)
        return x
    def _updateGeom(self,geom,ob):
        top,left=ob[0],ob[1]
        top*=self.cellSize
        left*=self.cellSize
        length=self.cellSize
        geom.v=([(left,top),(left+length,top),(left+length,top+length),(left,top+length)])
    def render(self):
        if(self.viewer==None):
            self.viewer=rendering.Viewer(self.totalSize * self.cellSize, self.totalSize * self.cellSize)
            self.agentGeom=[None]*self.agentSize
            self.goalGeom=[None]*self.agentSize
            for i in range(64):
                for j in range(64):
                    if (self.obstacles[i][j] == 1):
                        self._fillSquare(j,self.totalSize-i,(0,0,0))
            for i in range(len(self.agents)):
                self.agentGeom[i]=self._fillSquare(self.agents[i][1],self.agents[i][0],(255,0,0))
            for i in range(len(self.goals)):
                self.goalGeom[i]=self._fillSquare(self.agents[i][1],self.agents[i][0],(0,255,0))
        else:

            for i in range(len(self.agents)):
                self._updateGeom(self.agentGeom[i],self.agents[i])
            for i in range(len(self.goals)):
                self._updateGeom(self.goalGeom[i], self.goals[i])
        return self.viewer.render(return_rgb_array=0)

    def markAgents(self):
        self.agent_places = numpy.zeros([self.totalSize, self.totalSize]).astype(numpy.int32)
        for i in range(len(self.agents)):
            self.agent_places[self.agents[i,0],self.agents[i,1]] = 1

    def hot_agent(self, ind):
        ans = numpy.zeros([self.totalSize, self.totalSize]).astype(numpy.int32)
        ans[self.agents[ind][0], self.agents[ind][1]] = 1
        return ans

    def _get_obs(self):
        if(self.is_single):
            return numpy.stack([self.obstacles, self.goal_places, self.hot_agent(0)],axis=2)
        ans = []
        for i in range(len(self.agents)):
            ans.append(numpy.stack([self.obstacles, self.goal_places, self.hot_agent(i)],axis=2))
        return ans

    def reset(self):
        self.agents = numpy.zeros([self.agentSize, 2]).astype(numpy.int32)
        self.goals = numpy.zeros([self.agentSize, 2]).astype(numpy.int32)
        self.obstacles = numpy.zeros([self.totalSize, self.totalSize]).astype(numpy.int32)
        self.goal_places = numpy.zeros([self.totalSize, self.totalSize]).astype(numpy.int32)
        self.agent_places = numpy.zeros([self.totalSize, self.totalSize]).astype(numpy.int32)
        self.obstacles=obstacleMap[:]
        self.agents[0]=[40,63]
        self.goals[0]=[0,0]
        self.markAgents()
        self.viewer=None
        for i in self.goals:
            self.goal_places[i[0]][i[1]] = 1
        return self._get_obs()

    def step(self, actions):
        if(self.is_single):
            actions=[actions]
        for i in range(len(actions)):
            if (actions[i] == 0 and self.agents[i][0] != 0):  # up
                if (self.obstacles[self.agents[i][0] - 1][self.agents[i][1]] != 1):
                    self.agents[i][0] -= 1
            elif (actions[i] == 1 and self.agents[i][1] != 0):  # left
                if (self.obstacles[self.agents[i][0]][self.agents[i][1] - 1] != 1):
                    self.agents[i][1] -= 1
            elif (actions[i] == 2 and self.agents[i][1] != self.totalSize - 1):  # right
                if (self.obstacles[self.agents[i][0]][self.agents[i][1] + 1] != 1):
                    self.agents[i][1] += 1
            elif (actions[i] == 3 and self.agents[i][0] != self.totalSize - 1):  # down
                if (self.obstacles[self.agents[i][0] + 1][self.agents[i][1]] != 1):
                    self.agents[i][0] += 1
        self.markAgents()
        reward = 0
        for i in range(len(self.goals)):
            if (self.agent_places[self.goals[i][0], self.goals[i][1]] == 1):
                reward += 1
        done = (reward == self.agentSize)
        return self._get_obs(), reward, done, {}


